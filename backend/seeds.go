package main

import (
	"encoding/csv"
	"log"
	"os"

	"gorm.io/gorm"
)

type Seeder struct {
	db *gorm.DB
}

func (s Seeder) readFromCSV() {
	f, err := os.Open("data/countries.csv")
	if err != nil {
		log.Fatal(err)
	}

	// remember to close the file at the end of the program
	defer f.Close()

	// read csv values using csv.Reader
	csvReader := csv.NewReader(f)
	data, err := csvReader.ReadAll()
	if err != nil {
		log.Fatal(err)
	}

	for i, line := range data {
		if i > 0 { // omit header line
			var code string
			var name string

			for j, field := range line {
				if j == 0 {
					name = field
				} else if j == 1 {
					code = field
				}
			}
			s.seedCountry(name, code)
		}
	}
}

func (s Seeder) seedCountry(name string, code string) {
	s.db.Create(&Country{Name: name, Code: code})
}

func (s Seeder) SeedCountries() {
	s.readFromCSV()
}
